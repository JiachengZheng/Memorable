import Foundation
import UIKit

extension UIView{
    
    var width: CGFloat{
        return self.frame.size.width
    }
    
    var height: CGFloat{
        return self.frame.size.height
    }
    
    var size: CGSize{
        return self.frame.size
    }
    
    var originX: CGFloat{
        return self.frame.origin.x
    }
    
    var originY: CGFloat{
        return self.frame.origin.y
    }
    

    
    
    
    
    
    
    
    
    
    
}